int Map::getposdata(int x, int y)
{
	int returnstate;
	if (x < 0 || x>25 || y < 0 || y>10) {	//check for accessable area
		returnstate = 1;
	}else
	if (mapptr[x][y]== 2) {	//check for walls
		returnstate = 2;
	}else 
	if(mapptr[x][y]== 1) {	//check for mines
		mapptr[x][y]=0;
		returnstate = 3;
	}
	else {
		returnstate = 0;
	}
		return returnstate;
}




