int Map::getposdata(int x, int y)
{
	int returnstate;
	int map = *mapptr + x * 10 + y;
	if (x < 0 || x>25 || y < 0 || y>10) {	//check for accessable area
		returnstate = 1;
	}else
	if (map == 2) {	//check for walls
		returnstate = 2;
	}else 
	if(map == 1) {	//check for mines
		map = 0;
		returnstate = 3;
	}
	else {
		returnstate = 0;
	}
		return returnstate;
}


