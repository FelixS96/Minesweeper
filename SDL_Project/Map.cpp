int Map::checkpos(int x, int y)
{
	int returnstate;
	if (x == 0 || x==24 || y == 0 || y==(11 - 2)) {	//check for accessable area
		returnstate = 1;
	}
	return returnstate;
}
void Map::update(int x, int y)				//change
{
	covptr[x][y]=0;
	if(checkpos(x,y)==1)
	checkpos(x, y + 1);
	checkpos(x, y - 1);

	checkpos(x+1, y);
	checkpos(x+1, y + 1);
	checkpos(x+1, y - 1);

	checkpos(x-1, y);
	checkpos(x-1, y + 1);
	checkpos(x-1, y - 1);
	
}

int Map::getposdata(int x, int y)
{
	int returnstate;
	if (x < 0 || x>24 || y < 0 || y>11-2) {	//check for accessable area
		returnstate = 1;
	}else
	if (mapptr[x][y]== 2|| mapptr[x][y] == 3) {	//check for walls
		returnstate = 2;
	}else 
	if(mapptr[x][y]== 1) {	//check for mines
		mapptr[x][y] = 0;	//delete mine you stepped on
		returnstate = 3;
	}
	else {
		returnstate = 0;
	}
		return returnstate;
}






